    public function getCompanyByTaxCode($tax_code) {
        $url = 'https://api.vietqr.io/v2/business';
        $client = new Client([
            'headers' => [
                "Content-Type" => "application/json;charset=UTF-8",
            ],
        ]);

        $responseInvoice = $client->get($url . '/'.$tax_code,['verify' => false,]);
        $result = json_decode($responseInvoice->getBody());
        return response()->json($result->data);
    }
